    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Cliente c = new Cliente();
        c.setIdCliente(manCliente.verificaId());
        c.setNome(campoNome.getText());
        c.setRG(campoRG.getText());
        c.setTelefone(campoTel.getText());
        
        if(campoSexo.getSelectedItem().toString().equals("Feminino")){
             c.setSexo('F');
        }else{
            c.setSexo('M');
        }
        //listaDeClientes.add(c);
        //verificaArray();
        manCliente.cadastrarCliente(c);
        JOptionPane.showMessageDialog(getContentPane(), "Cliente cadastrado com sucesso!!!");
        campoId.setText(String.valueOf(c.getIdCliente()));
    }//GEN-LAST:event_btSalvarActionPerformed

