    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = campoPesquisar.getText();
        boolean verificaCliente=false;
        ArrayList<Cliente> arrayCliente = manCliente.lerCliente();
        for(int i=0;i<arrayCliente.size();i++){
             if(arrayCliente.get(i).getNome().equalsIgnoreCase(nome)){
                 campoNome.setText(arrayCliente.get(i).getNome());
                 campoId.setText(String.valueOf(arrayCliente.get(i).getIdCliente()));
                 campoRG.setText(arrayCliente.get(i).getRG());
                 campoTel.setText(arrayCliente.get(i).getTelefone()); 
                 verificaCliente=true;
             }
        }
        if(verificaCliente==true){
             JOptionPane.showMessageDialog(getContentPane(), "Cliente encontrado com sucesso!!!");
        }else{
             JOptionPane.showMessageDialog(getContentPane(), "Cliente não encontrado!!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btExcuirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcuirActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(campoId.getText());
        boolean verificaCliente=false;
        ArrayList<Cliente> arrayCliente = manCliente.lerCliente();
        for(int i=0;i<arrayCliente.size();i++){
             if(arrayCliente.get(i).getIdCliente()==id){
                 arrayCliente.remove(i);
                 manCliente.reecreverArquivo(arrayCliente);
                 verificaCliente=true;
             }
        }
        if(verificaCliente==true){
             JOptionPane.showMessageDialog(getContentPane(), "Cliente excluído com sucesso!!!");
        }else{
             JOptionPane.showMessageDialog(getContentPane(), "Cliente não encontrado!!!");
        }
        
    }//GEN-LAST:event_btExcuirActionPerformed

